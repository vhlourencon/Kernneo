package br.com.kernneo.desktop.view.widget;

import javax.swing.Icon;
import javax.swing.JLabel;

public class JLabelPad extends JLabel {

    private static final long serialVersionUID = 1L;

    public JLabelPad() {
	super();
    }

    public JLabelPad(String arg0) {
	super(arg0);
    }

    public JLabelPad(String arg0, int arg1) {
	super(arg0, arg1);
    }

    public JLabelPad(Icon arg0) {
	super(arg0);
    }

    public JLabelPad(Icon arg0, int arg1) {
	super(arg0, arg1);
    }

    public JLabelPad(String arg0, Icon arg1, int arg2) {
	super(arg0, arg1, arg2);
    }

}
